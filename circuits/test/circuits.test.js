// ── Constants ─────────────────────────────────────────────────────────────────
const TREE_DEPTH    = 20;
const DENOMINATION  = 1_000_000_000n; // 1 SOL in lamports

// ── Check build artifacts exist ───────────────────────────────────────────────
function artifactsExist(wasm, zkey, vk) {
    return fs.existsSync(wasm) && fs.existsSync(zkey) && fs.existsSync(vk);
}

// ── Global Poseidon instance ──────────────────────────────────────────────────
let poseidon;
let F; // finite field helper

beforeAll(async () => {
    poseidon = await buildPoseidon();
    F = poseidon.F;
}, 30_000);

// ── Poseidon helper — returns BigInt ─────────────────────────────────────────
function poseidonHash(...inputs) {
    const result = poseidon(inputs.map(x => F.e(x)));
    return BigInt(F.toObject(result));
}

// ── Random field element (< BN254 scalar field order) ────────────────────────
function randomFieldElement() {
    // BN254 scalar field order
    const p = 21888242871839275222246405745257275088548364400416034343698204186575808495617n;
    // Generate 32 random bytes and reduce mod p
    const bytes = new Uint8Array(32);
    for (let i = 0; i < 32; i++) bytes[i] = Math.floor(Math.random() * 256);
    let value = 0n;
    for (const b of bytes) value = (value << 8n) | BigInt(b);
    return value % p;
}

// ── Incremental Merkle tree (mirrors on-chain logic) ─────────────────────────
// ZEROS[i] = Poseidon(ZEROS[i-1], ZEROS[i-1]), ZEROS[0] = 0
// These must match the on-chain Rust ZEROS constant (generated by scripts/generate_zeros.js).
function buildZeros(depth) {
    const zeros = new Array(depth);
    zeros[0] = 0n;
    for (let i = 1; i < depth; i++) {
        zeros[i] = poseidonHash(zeros[i - 1], zeros[i - 1]);
    }
    return zeros;
}

class IncrementalMerkleTree {
    constructor(depth) {
        this.depth = depth;
        this.zeros = buildZeros(depth);
        this.filledSubtrees = [...this.zeros];
        this.roots = [this._zeroRoot()];
        this.nextIndex = 0n;
    }

    _zeroRoot() {
        let hash = 0n;
        for (let i = 0; i < this.depth; i++) {
            hash = poseidonHash(hash, this.zeros[i]);
        }
        return hash;
    }

    // Insert a leaf and return its Merkle path.
    insert(leaf) {
        let currentHash = BigInt(leaf);
        let currentIndex = this.nextIndex;
        const pathElements = [];
        const pathIndices = [];

        for (let i = 0; i < this.depth; i++) {
            pathIndices.push(Number(currentIndex % 2n));
            if (currentIndex % 2n === 0n) {
                pathElements.push(this.zeros[i]);
                this.filledSubtrees[i] = currentHash;
                currentHash = poseidonHash(currentHash, this.zeros[i]);
            } else {
                pathElements.push(this.filledSubtrees[i]);
                currentHash = poseidonHash(this.filledSubtrees[i], currentHash);
            }
            currentIndex = currentIndex / 2n;
        }

        this.roots.push(currentHash);
        this.nextIndex += 1n;

        return {
            pathElements,
            pathIndices,
            root: currentHash,
            leafIndex: this.nextIndex - 1n,
        };
    }

    currentRoot() {
        return this.roots[this.roots.length - 1];
    }
}

// ── Deposit proof helpers ─────────────────────────────────────────────────────

function generateDepositInputs(denomination = DENOMINATION) {
    const nullifier = randomFieldElement();
    const secret    = randomFieldElement();
    const commitment = poseidonHash(nullifier, secret, denomination);
    return { nullifier, secret, denomination, commitment };
}

async function generateDepositProof(inputs) {
    const { proof, publicSignals } = await snarkjs.groth16.fullProve(
        {
            commitment:   inputs.commitment.toString(),
            denomination: inputs.denomination.toString(),
            nullifier:    inputs.nullifier.toString(),
            secret:       inputs.secret.toString(),
        },
        DEPOSIT_WASM,
        DEPOSIT_ZKEY,
    );
    return { proof, publicSignals };
}

// ── Withdraw proof helpers ────────────────────────────────────────────────────

function generateWithdrawInputs(tree, denomination = DENOMINATION) {
    const nullifier     = randomFieldElement();
    const secret        = randomFieldElement();
    const recipient     = randomFieldElement(); // wallet as field element
    const relayerAddr   = randomFieldElement();
    const relayerFeeMax = 83_000n; // ~0.000083 SOL

    const commitment         = poseidonHash(nullifier, secret, denomination);
    const nullifierHash      = poseidonHash(nullifier);
    const withdrawalCommitment = poseidonHash(relayerAddr, relayerFeeMax, recipient);

    const { pathElements, pathIndices, root } = tree.insert(commitment);

    return {
        // public
        nullifierHash,
        root,
        withdrawalCommitment,
        // private
        nullifier,
        secret,
        denomination,
        pathElements,
        pathIndices,
        recipient,
        relayerAddress: relayerAddr,
        relayerFeeMax,
    };
}

async function generateWithdrawProof(inputs) {
    const circomInputs = {
        nullifierHash:         inputs.nullifierHash.toString(),
        root:                  inputs.root.toString(),
        withdrawalCommitment:  inputs.withdrawalCommitment.toString(),
        nullifier:             inputs.nullifier.toString(),
        secret:                inputs.secret.toString(),
        denomination:          inputs.denomination.toString(),
        pathElements:          inputs.pathElements.map(x => x.toString()),
        pathIndices:           inputs.pathIndices.map(x => x.toString()),
        recipient:             inputs.recipient.toString(),
        relayerAddress:        inputs.relayerAddress.toString(),
        relayerFeeMax:         inputs.relayerFeeMax.toString(),
    };

    const { proof, publicSignals } = await snarkjs.groth16.fullProve(
        circomInputs,
        WITHDRAW_WASM,
        WITHDRAW_ZKEY,
    );
    return { proof, publicSignals };
}

async function verifyWithdraw(proof, publicSignals) {
    const vkey = JSON.parse(fs.readFileSync(WITHDRAW_VK, "utf8"));
    return snarkjs.groth16.verify(vkey, publicSignals, proof);
}

async function verifyDeposit(proof, publicSignals) {
    const vkey = JSON.parse(fs.readFileSync(DEPOSIT_VK, "utf8"));
    return snarkjs.groth16.verify(vkey, publicSignals, proof);
}

