// ── T11 — groth16_verify CU cost ─────────────────────────────────────────────
// Note: the dummy proof will fail to verify (expected — we only measure CU).
// The TypeScript test reads unitsConsumed from the simulation even on failure.
pub fn run_groth16_benchmark() -> Result<()> {
    let mut verifier = Groth16Verifier::new(
        &DUMMY_PROOF_A,
        &DUMMY_PROOF_B,
        &DUMMY_PROOF_C,
        &DUMMY_INPUTS,
        &WITHDRAW_VK,
    )
    .map_err(|_| error!(ErrorCode::BenchmarkVerifierInit))?;

    // We call verify and ignore the result — we only want CU measurement.
    // The dummy proof is not a valid proof against this VK.
    let _ = verifier.verify();

    msg!("T11: groth16_verify CU consumed (proof invalid — expected for benchmark)");
    Ok(())
}

// ── T12 — 20-level Poseidon CU cost via native sol_poseidon syscall ───────────
// Uses anchor_lang::solana_program::poseidon::hashv (BN254-X5, BigEndian)
// which maps to the sol_poseidon syscall on-chain — no stack overflow.
pub fn run_poseidon_benchmark() -> Result<()> {
    let leaf: [u8; 32] = [1u8; 32];
    let mut current = leaf;

    for level in 0..20usize {
        let hash = hashv(
            Parameters::Bn254X5,
            Endianness::BigEndian,
            &[&current, &ZEROS[level]],
        )
        .map_err(|_| error!(ErrorCode::BenchmarkPoseidonHash))?;
        current = hash.0;
    }

    msg!("T12: 20-level Poseidon OK, root[..4]={:x?}", &current[..4]);
    Ok(())
}
